        $("#selectEmployee").selectpicker({
            actionsBox: true,
            selectAllText: "<?php echo e(__('modules.permission.selectAll')); ?>",
            deselectAllText: "<?php echo e(__('modules.permission.deselectAll')); ?>",
            multipleSeparator: " ",
            selectedTextFormat: "count > 8",
            countSelectedText: function(selected, total) {
                return selected + " <?php echo e(__('app.membersSelected')); ?> ";
            }
        });

        $('#department_id').change(function() {
            var id = $(this).val();
            var rotation = $('#rotation').val();
            var url = "<?php echo e(route('employees.by_department', ':id')); ?>";
            url = url.replace(':id', id);

            $.easyAjax({
                url: url,
                container: '#save-automated-data-form',
                type: "GET",
                blockUI: true,
                data: {
                    request_from: 'rotation',
                    department_id: id,
                    rotation: rotation,
                },
                success: function(response) {
                    if (response.status == 'success') {
                        $('#selectEmployee').html(response.data);
                        $('#selectEmployee').selectpicker('refresh');
                    }
                }
            });
        });

        $('#save-automated-form').click(function() {
            const url = "<?php echo e(route('shift-rotations.store_automate_shift')); ?>";

            $.easyAjax({
                url: url,
                container: '#save-automated-data-form',
                type: "POST",
                disableButton: true,
                blockUI: true,
                file: true,
                buttonSelector: "#save-automated-form",
                data: $('#save-automated-data-form').serialize(),
                success: function(response) {
                    if (response.status == 'success') {
                        let url = "<?php echo e(route('attendance-settings.index')); ?>?tab=shift-rotation";
                        window.location.href = url;
                    }
                }
            });
        });

        init(RIGHT_MODAL);
    });
